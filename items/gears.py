

class GearAttribute():
    def __init__(self, bg, name, kind, level, stars, rating, fragments_numerator, fragments_denominator, attribute_add_count, attribute_icon_list):
        self.bg = bg
        self.name = name
        self.kind = kind
        self.level = level
        self.stars = stars
        self.rating = rating
        self.fragments_numerator = fragments_numerator
        self.fragments_denominator = fragments_denominator
        self.attribute_add_count = attribute_add_count
        self.attribute_icon_list = attribute_icon_list
